package cn.brimon.model;

public class Repo {
	Integer repoId;
	String repoName;
	public Integer getRepoId() {
		return repoId;
	}
	public void setRepoId(Integer repoId) {
		this.repoId = repoId;
	}
	public String getRepoName() {
		return repoName;
	}
	public void setRepoName(String repoName) {
		this.repoName = repoName;
	}
	
	
}
